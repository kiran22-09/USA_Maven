package com.ablelogix.Orange_Hrm.class1;

public class Calculator {
	

		
		public int addition(int oper1, int oper2) {
			return oper1+oper2;
		}
		
		public int subtraction(int oper1, int oper2) {
			return oper1-oper2;
		}
		
		public int multiplication(int oper1, int oper2) {
			return oper1*oper2;
		}
		
		public int division(int oper1, int oper2) {
			return oper1/oper2;
		}

	


}
